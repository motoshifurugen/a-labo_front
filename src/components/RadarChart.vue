<script>
import { Radar, mixins } from 'vue-chartjs'
const { reactiveProp } = mixins

export default {
    extends: Radar,
    mixins: [reactiveProp],
    props: ['options'],
    mounted () {
        if (this._chart) {this._chart.destroy()}
        this.renderChart(this.chartData, this.options)
    }
}
</script>
